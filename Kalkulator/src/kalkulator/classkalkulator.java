public class classkalkulator {
    public int a, b;

    
    public int penjumlahan(){
        int hasil;
        hasil = this.a + this.b;
        return hasil;  
    } 
    public int pengurangan(){
        int hasil;
        hasil = this.a - this.b;
        return hasil;  
    }
    public int perkalian(){
        int hasil;
        hasil = this.a * this.b;
        return hasil;  
    } 
    public int pembagian(){
        int hasil;
        hasil = this.a/this.b;
        return hasil;  
    }
}

